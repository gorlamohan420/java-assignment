public class Pro2 {
    public static void main(String[] args) {
        int num = 5;
        System.out.println("Original value: " + num);

        // Increment operator (++)
        num++;
        System.out.println("After increment: " + num);

        // Decrement operator (--)
        num--;
        System.out.println("After decrement: " + num);
    }
}
