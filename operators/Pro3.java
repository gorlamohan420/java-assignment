public class Pro3 {
    public static void main(String[] args) {
        int num1 = 5;
        int num2 = 10;

        // Equal operator (==)
        if (num1 == num2) {
            System.out.println("Numbers are equal.");
        } else {
            System.out.println("Numbers are not equal.");
        }

        // Not equal operator (!=)
        if (num1 != num2) {
            System.out.println("Numbers are not equal.");
        } else {
            System.out.println("Numbers are equal.");
        }
    }
}
